    var a = 22F;
    var b = 7f;
    var x = 3;
    var y = 4;
    var z = 5;
    println(a/b)
    println(a%b)
    println("the result of x+y is : ${x+y}")
    println("the result of x-y is : ${x-y}")
    println("the result of x+y+z is : ${x+y+z}")

    println(15.0f / 7.0);
